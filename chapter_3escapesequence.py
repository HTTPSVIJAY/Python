#escape sequence\n
story = ("there are many problems.\nso that we have to do work hard")
print(story)
#escape sequence\t
story = ("there are many problems.\tso that we have to do work hard")
print(story)
#escape sequence \'
story = ("there are many problems.\'so that we have\' to do work hard")
print(story)
#escape sequence \\
story = ("there are many problems.\\so that we have to do work hard")
print(story)